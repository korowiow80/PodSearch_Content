<h3>Procedural Complications and Risks</h3>
<p>The Whipple procedure has been refined from the original two step-processes to a one step-operation. Although modern health care technology has greatly improved the procedure and reduced the mortality rate to 5%, a Whipple procedure is still a major surgical procedure with risks and <a href="http://www.whipple-procedure.org/whipple-procedure-complications/">complications</a>.</p>
<p>Possible immediate complications are:</p>
<ul>
<li>Anesthesia-related effects</li>
<li>Chest infection and/or breathing problems</li>
<li>Bleeding during or after operation</li>
<li>Wound infection</li>
<li>Blood clots in extremities</li>
<li><a href="http://www.nlm.nih.gov/medlineplus/ency/article/002231.htm" target="_blank">Anastomotic</a> leak</li>
<li>Delayed emptying of stomach</li>
</ul>
<p>After the surgery, patients stay in the hospital for 1-2 weeks. Close monitoring of vital signs and possibility of complications is vital. Patients are fed IV fluids to give time for the digestive tract to heal before it is stretched by food. Painkillers are given and other measures of care are provided.</p>
<h3>Prognosis</h3>
<p>The Whipple procedure, among other treatment options available, gives the patient the best chances of cure. The surgery is aimed at the complete elimination of the source, that’s why a wide area surrounding the neoplasm is removed. </p>
<p>Without surgery, only 6% of patients with pancreatic cancer can survive up to 5 years. A successful surgery increases this by 10-40%. On average, the Whipple procedure survival rate is 20%.</p>
<p>The <a href="http://www.whipple-procedure.org/whipple-procedure-prognosis/">prognosis after a Whipple procedure</a> is determined by many factors. And even if the visible tumor is successfully removed, some cancer cells are not discovered prior and during the surgery. It is possible that cancer cells have spread to other organs and start to grow after the procedure. To counteract this and increase the chances of a better prognosis, patients are advised to undergo chemotherapy and/or radiation after surgery.</p>
