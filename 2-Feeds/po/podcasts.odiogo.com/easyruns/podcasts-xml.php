      <content:encoded><![CDATA[<p><img class="alignleft size-full wp-image-335" title="nike ipod sport kit for running shoes" src="http://www.easyruns.com/wp-content/uploads/2009/05/nike_ipod_sport_kit.jpg" alt="nike ipod sport kit for running shoes" width="280" height="280" />If you have been wanting to enhance your workout experience by hiring a personal trainer, you don’t have to spend hundreds of dollars doing so.  Instead, you can invest in the Apple Nike + iPod Sport Kit for your iPod which is like a mini digital trainer.</p>
<p>The iPod Sport Kit features a wireless sensor for Nike+ shoes as well as a wireless receiver for your iPod nano.  All you have to do is insert the wireless sensor inside the pocket under the insole of your Nike+ exercising shoe and then plug the wireless receiver in the Dock connector on the nano.  Once everything is connected, you can begin working out.  Whether you are running on a treadmill or walking outside, the sensor will send all the information of your performance to your iPod.  This information includes time, distance, pace and how many calories you burned.</p>
<p>When you return to your computer, you can transfer your workout information from your iPod  to iTunes and NikePlus to analyze it all.  Over time you can view how you have performed during your workouts and make any changes next time you exercise.  You can set new goals and even be able to participate in a virtual race with other runners.</p>
<p>Now you can not only listen to your favorite tunes during a workout routine, but you can also monitor your performance.  You can also enjoy real time tracking that provides feedback to inform you of your progress during the workout.  The Apple Nike+ and iPod Sport Kit is compatible with iPod nano 1G, 2G, 3G, 4G as well as iPod touch 2G.  It is important to note that the iPod touch 2nd Generation does not require the use of a receiver as it comes with a built-in support for the kit.  With the Nike+ and iPod Sport kit, you don’t need to think about getting that expensive personal trainer from your gym anymore.</p>
