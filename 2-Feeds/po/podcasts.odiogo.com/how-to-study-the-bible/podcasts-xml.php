<p>Paul the Apostle of Jesus Christ penned the following words in his epistle to a disciple hight John:</p>
<p>1 John</p>
<p>2 ;22 Who is a liar but he that denieth that Jesus is the Messiah? He is antichrist, that denies the Father and the Son. </p>
<p>The words, “Christ” “Messiah”, and “Anointed” all carry the same idea.  They point to the practice of pouring oil atop the head, of a new king or high priest.</p>
<p>By the verse above, if anybody denies that Jesus is king and high priest, then they deny that he is God, and they are not a Christian.  Moreover, if they deny that Christ is The Almighty, then they are not a worshipper of Christ.  These ideas are implied by the Bible’s verse, above.</p>
<p>2nd Means Of Culling A Phony Christian Person</p>
<p>Great numbers of human beings avow that they are worshippers of Christ, and many say that they love Jesus, or that they have allowed him into their heart.  Nevertheless the Bible’s verses, following, teach us that some of those creatures might not be followers of Jesus, regardless of what they claim to be.</p>
<p>In the following Bible verses, it is Christ Jesus himself speaking:</p>
<p>Matthew 7</p>
<p> 21 Not every person that utters to me, Lord, Lord, will enter into the kingdom of heaven; but he who doeth the will of my Father who is in heaven. </p>
<p> 22 Many souls will say to me in that day, Master, Master, haven&#8217;t we prophesied in thy name? and in thy name have cast out devils? and in thy name done many wonderful doings?</p>
<p> 23 And then will I profess to them, I never knew you: leave from me, you lawless ones. </p>
<p>John 14</p>
<p> 21 He who hath my commandments, and keepeth them, he it is who loves me: and he that loves me will be loved by my Father, and I will love him, and will manifest myself to him. </p>
<p> 23 … If a man love me, he will keep my words: and my Father will love him, and we will come to him, and make our dwelling with him. </p>
<p> 24 He that doesn&#8217;t love me doesn&#8217;t keep my sayings: and the saying which ye hear is not mine, but the Father&#8217;s who sent me. </p>
<p>The verses from the Book of Isaiah, the prophet, below, make it obvious to us how anybody can be a worshipper of Christ, if they make up their mind to be one, not just the house, of a man named Israel.  </p>
<p>A prophet is somebody who says what God told him to say.  So, in effect, it is God speaking.  Remembering who isn’t or who is a worshipper of Christ, in your life, can be of help to you, when you are learning how to study the Bible</p>
<p>Isaiah 56</p>
<p> 2 Blessed is the man … that keepeth the sabbath from polluting it, and keeps his hand from doing any evil. </p>
<p> 3 Neither let the son of the stranger, that hath joined himself to the LORD, speak, saying, Jahweh hath utterly separated me from his people…. </p>
<p>[“His people” = the children of a man named “Israel”]</p>
<p> 6 Also the sons of the stranger, that join themselves to the LORD, to serve him, and to love the name of Jahweh, to be his servants, every one that keepeth the sabbath from polluting it, and taketh hold of my covenant;</p>
<p>[“stranger” = not the children of a man named “Israel”]</p>
<p> 7 Even them will I bring to my holy mountain, and make them joyful ….</p>
<p> 8 The Lord GOD, which gathereth the outcasts of Israel says, Yet will I gather others to him, beside those that are gathered unto him.</p>
<p>In the passage above, the word “Israel” means “the children of a man named ‘Israel’”.</p>
