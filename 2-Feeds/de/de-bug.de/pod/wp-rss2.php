<p><a href="http://de-bug.de/pod/files/2012/05/brq083k.jpg"><img class="alignnone size-full wp-image-7141" title="brq083k" src="http://de-bug.de/pod/files/2012/05/brq083k.jpg" alt="" width="250" height="250" /></a></p>
<p>Mit 20 Jahren Techno-Historie zwischen Berlin und London, hat Werner Niedermeier eine so bewegte Geschichte, dass man gar nicht weiß, wo man mit dem Name-Dropping beginnen soll: bei der Latte an Remixen (für Künstler von Terry Callier über Robert Owens bis Guy Gerber), den unzähligen DJ-Gigs (Panoramabar bis hin zur eigenen Residenz in Londons The End) oder der Unmenge an eigenen Produktionen (für Labels wie Boxer oder Resopal bishin zu EFA). Ein Mann mit Geschichte also, und das hört man auf seiner neuen Sub Culture EP auf Broque. Exzessiver Sampleeinsatz generiert hier markante Techno-Tracks, die mit ihrer old-schoolig perkussiven und zugleich sehr groovenden Art geradezu nach einem gut geölten Soundsystem und Peaktime-Floors verlangen. Das reicht vom charakterstarken Beat-Box Techno „Always Crowded“ bis hin zum eindeutigen Abräumer „Sub Culture“, der mit leicht verspulter Atmosphäre, funky aber doch stets marschierenden Beats und subtilen Garage-Anklängen Garant für die totale Tanzflächen-Hysterie ist. 20 Jahre Cluberfahrung werden hier auf 7 Minuten zum amtlichen Floorfiller kondensiert. „Sub Culture“ Remixes unserer langjährigen Begleiter Praktika und Granlab runden das Paket mit ihren persönlichen Noten ab und lassen das Pendel dabei mal mehr in Richtung Dub mal mehr in Richtung linearer Techno-Effizienz ausschlagen. Wunderbar.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>20 years of personal techno history in between London and Berlin left their marks all over the place and make it impossible to figure out where to begin the name dropping when introducing Werner Niedermeier: with his numerous remixes (for the likes of Terry Callier, Robert Owens or Guy Gerber), the uncountable DJ-gigs (from Panoramabar up to his very own residency at Mr C’s The End) or his endless list of productions (for labels like Boxer, Resopal or EFA). A man with a past, that becomes even more apparent when listening to his new Sub Culture EP for Broque. Excessively making use of samples, he generates techno tracks which are both original and full of character. Percussive old-school yet grooving, his tracks demand a well tuned soundsystem as well as a club crowd which closes in on peaktime madness. The significant opener “Always Crowded” can easily be described as playful beat box techno, while the un-contested star of the EP is its title track: “Sub Culture” features a semi-psychedelic atmosphere together with marching beats and somewhat subtle garage references and has the capability to drive any dancefloor into complete hysteria. 20 years club experience, condensed into a 7 minute floor filler. “Sub Culture” Remixes from two of our old mates conclude the package where Granlab’s version leans more towards dub (Granlab) and Praktika applies linear techno efficiency. Fantastic.</p>
