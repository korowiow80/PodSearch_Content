				<h2 class="post-title"><a href="http://www.moviesbywomen.com/woman-directors-history.php" rel="bookmark">Woman Directors in History</a></h2>

					

				<div class="postmeta">

					<a href="http://www.moviesbywomen.com/woman-directors-history.php">January 24, 2012</a> /

					Norman /

					<a href="http://www.moviesbywomen.com/woman-directors-history.php#comments">No comments</a>

					
				</div>

				

				<div class="entry">

					
					<p>Almost all the well known directors are male. Just name it: Hitchcock, Welles, Spielberg, Kubrick. It&#8217;s no great mystery that the contribution of women to film directing is often overlooked by fans. <a href="http://www.nriol.net/">Health insurance for visitors in USA</a> is a must when going overseas. Although small in number, their contributions of female directors to the industry is huge. One of the contemporary acclaimed female directors is Sofia Coppola, of the Coppola family fame (his father, Francis, directed the Godfather trilogy). She has had an illustrious career despite her young age: the 2003 film Lost in Translation remains one of her crowning achievement. Her film Somewhere (2010) won the Golden Lion at the Venice Film Festival.</p>
<p>Claire Denis directed Chocolat (1998), a French film which was nominated for the Academy Award best picture. Jane Campion directed the poignant film The Piano (1993), which won the top award at the Cannes and three Academy Awards. Kathryn Bigelow&#8217;s The Hurt Locker (2008) won the Oscar for Best Picture and bagged her the Best Director award. Women have been known as great filmmaker in the past too: the great German filmmaker Leni Riefenstahl was the favorite choice of Hitler to do his propaganda work. She directed Triumph des Willens (Triumph of the Will), a Nazi propaganda piece that have been critically praised for its cinematic brilliance.</p>

