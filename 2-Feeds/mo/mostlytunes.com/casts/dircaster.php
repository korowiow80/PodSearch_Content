<p>One of the things that first got me interested in podcasting was the ability to play music. Music&#8217;s always been a big part of my life, and I&#8217;ve always liked indie stuff anyway, so podcasting was a great way to join these things together.  I was involved in early discussions to form <a href="http://www.musicpodcasting.org" target="_blank">AMP</a> &#8211; the association of music podcasting in 2004, didn&#8217;t like where the rules were going or being hemmed in, walked away, created a show (<a href="http://www.mostlytunes.com" target="_blank">mostlytunes</a>) as my way of &#8216;doing my own thing&#8217;, then after most of the initial crowd were gone, joined AMP. I think AMP is podcasting&#8217;s best kept secret right now, it&#8217;s a core group of experienced &amp; devoted music fans, and they really seem to be having fun. Funny how things work out.</p>
<p>It may seem strange to many that music was what drew me to podcasting. As the last couple of years have shown, I haven&#8217;t been very active in music podcasting, choosing to concentrate my limited time on community shows and events such as <a href="http://www.canadianpodcastbuffet.ca">CPB</a>, podcamps, <a href="http://www.podcastersacrossborders.com/">PAB</a> and things like that..</p>
<p>Today I went to the mostlytunes PO Box (and the Canadapost people are very nice about keeping packages for me longer than they should) and brought home a few CDs that were sent in :</p>
<p align="center"> <a href="http://www.flickr.com/photos/bobandaj/3010319431/" title="photo sharing"><img src="http://farm4.static.flickr.com/3029/3010319431_59c3edd268_m.jpg" style="border: 2px solid #000000" height="257" width="340" /></a></p>
<p>I think I&#8217;ve put out 1 or 2  music episodes of my own in the last year, and I&#8217;m thinking maybe the universe is telling me it wants me to start playing tunes again. So I&#8217;m going to put 6 mostlytunes episodes out over the next 6 weeks. Let&#8217;s see if it still has traction, and more importantly, if I still enjoy doing it. Let&#8217;s find out together, k?</p>
<p>Watch www.mostlytunes.com this weekend for the first episode of this series..</p>

