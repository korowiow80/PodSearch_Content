Qualifying
(http://languagecaster.com/wp-content/uploads/PA-11813332-500x340-300x204.jpg)
Teams sometimes suffer a dip in form after winning a major tournament but Spain followed up their World Cup 2010 victory with a flawless European qualifying campaign. They easily topped their group with a 100% record - they won all 8 of their matches against Scotland, Lithuania, Liechtenstein and the Czech Republic.


Key Players

Where to begin? The Spanish national team almost picks itself such is the consistency of its high-performing players. The Barca quintet of Xavi, Busquets, Pedro and Iniesta will of course be there, while Madrid&#039;s Casilas, Ramos, and Albiol will bolster the defence but in this tournament they will be missing two key players: David Villa and Carles Puyol. Puyol is an inspirational figure at the back and will be sorely missed but goalscorer Villa is probably a greater loss. He has scored 10 goals in 11 games at the previous two major tournaments - 4 in Euro 2008 and 6 more in 2010 - and though Spain do have Bilbao&#039;s Llorente and the 2008 winning goalscorer Torres in their team they will miss Villa.
The Manager
(http://languagecaster.com/wp-content/uploads/787405-13422665-640-360-300x168.jpg)
Taking over from Euro 2008 winning manager Luis Aragones could seem like a poisoned chalice - surely the only way is down after such a wonderful victory? Step forward Vicente del Bosque regarded by many as a safe pair of hands, someone who lets the players play as he trusts in their ability. After winning his first ten games in charge of La SelecciÃ³n - a national record - del Bosque oversaw the World Cup win in South Africa with his side refusing to change their passing style throughout the tournament.
Their Chances
They are the favourites to win Euro 2012 though injuries and the fact that no team has ever retained the European Championship or won three major tournaments in a row means that this competition could be their hardest challenge yet. Though their group is not a group of death it will be tricky enough with powerhouses Italy, dark horses Croatia and well-organised Ireland all ready to stop the Spanish. The first match is vital and if they can get something from that game against Italy then they should easily progress to the knock-out stages. After that, depending on injuries and luck, they should be there or thereabouts again.
