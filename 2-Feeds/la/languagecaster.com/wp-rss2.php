The Good
Liverpool won a trophy, there is some silverware in the cabinet, and that hasn&#039;t happened since the FA Cup win in 2007. OK, it is the least important cup in the English top flight, the Carling Cup, but it was a welcome victory. On the way, Liverpool beat Chelsea and Manchester City, so they did it the hard way, too. Liverpool also had another good cup run, this time in the FA Cup, and they weren&#039;t far off winning it either only losing 2-1 in the final to Chelsea. On the way to the final they claimed the scalps of Manchester United, their huge rivals, and Everton, their local rivals. So again, that was cause to feel pleased with their performance. Andy Carroll scored in the semi finals against Everton and in the final against Chelsea, too - signs of hope for next year for the big centre forward.
The Bad
