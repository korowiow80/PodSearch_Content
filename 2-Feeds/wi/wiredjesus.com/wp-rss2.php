			<content:encoded><![CDATA[<p><img src="http://rachelheldevans.com/assets/images/billy-graham-amendment-one.jpg" alt="lost generation" /></p>
<p><a href='http://rachelheldevans.com/win-culture-war-lose-generation-amendment-one-north-carolina'>Rachel Held Evans | How to win a culture war and lose a generation</a>.</p>
<p>Once again my friend, Rachel Held Evans, nails it.  I remember reading about &#8220;The Lost Generation&#8221; of the 1920s and we have created our own lost generation in the church in the name of pure doctrine and social justice.  I think there are really two conflicted poles that have created the same result &#8211; a generation alienated from their Boomer parents and grandparents.  Yeah, I know I harp on the Boomers a lot.  Its an old song but when you look who is abandoning congregations, its those under 40, usually in response to the polarities &#8211; intolerance in the guise of doctrine or self-righteous correctness masquerading as justice/social Gospel. </p>
<p>As Rachel puts it so well:<br />
My generation is tired of the culture wars. </p>
<p>We are tired of fighting, tired of vain efforts to advance the Kingdom through politics and power, tired of drawing lines in the sand, tired of being known for what we are against, not what we are for. </p>
<p>So my question for those evangelicals leading the charge in the culture wars is this: Is it worth it? </p>
<p>Is a political “victory” really worth losing millions more young people to cynicism regarding the Church?</p>
<p>Is a political “victory” worth further alienating people who identify as LGBT?</p>
<p>Is a political “victory” worth perpetuating the idea that evangelical Christians are at war with gays and lesbians?</p>
<p>And is a political “victory” worth drowning out that quiet but persistent internal voice that asks—what if we get this wrong? </p>
<p>Too many Christian leaders seem to think the answer to that question is “yes,” and it&#8217;s costing them.  </p>
<p>Because young Christians are ready for peace.</p>
<p>We are ready to lay down our arms. </p>
<p>We are ready to start washing feet instead of waging war.  </p>
<p>And if we cannot find that sort of peace within the Church, I fear we will look for it elsewhere. </p>
<p>WJP Addendum: If we don&#8217;t recognize it, it will be the epitaph of the North American Church &#8211; cause of death &#8211; war wounds.</p>
