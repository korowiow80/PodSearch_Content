			<content:encoded><![CDATA[<p><img src="http://4.bp.blogspot.com/-LHDxqigCco4/TpeLXt1w0GI/AAAAAAAADf8/4_aWa4D_QEE/s400/fry.jpg" alt="offended" /></p>
<p><a href='http://shetterly.blogspot.com/2012/04/expanded-stephen-fry-on-being-offended.html'>it&#8217;s all one thing: Stephen Fry on being offended, plus Penn &#038; Teller</a>.</p>
<p>Great blog post with a couple of video clips on one of the great banes and logical fallacies of American culture &#8211; the statement &#8220;I am offended by that.&#8221;  This Stephen Fry quote and poster has gone viral and I think that is a good thing.</p>
<p>As he observes, we have come to believe that if you are offended by something, you are inherently granted the power to trounce on any offenders rights.  I&#8217;m offended, you must stop doing that.  I&#8217;m offended, therefore what you said is wrong.</p>
<p>The sad part is that we have conditioned ourselves, particularly in the church, into believing that every offended person, every complaint, is worthy of being responded to and we obligated to conform to it in practice &#8211; be it worship, education, or preaching.  And we somehow convince ourselves that is is what Jesus would do, as if Jesus answered and changed his ministry to meet the complaints and new expectations of the offended Romans, Sadduccess, Pharisees, moneychangers, crowds, Judas, Sanhedrin, Pontius Pilate, teachers of the law, his family, the high priest, and a few others that are mentioned in the Gospels.  As I recall, it didn&#8217;t quite work out that way and the offended parties nailed him to a cross.  Most were equally offended when he had the gall to rise from the dead, which just magnified the fact that their perspectives and actions were wrong but that he still loved them, calling them to follow.</p>
<p>Of course, the problem in it is all is the old Dr. Phil line, &#8220;Its not about you!&#8221;  You and I are not the center of the universe nor are we God.  But as soon as we say &#8220;I&#8217;m offended&#8221; and demand someone else, either the &#8220;offender&#8221; or some governing body &#8220;do something&#8221; about it, we place ourselves and our sensibilities over everyone else, including God.  If you didn&#8217;t quite pick up on it, Jesus offended nearly everyone around him but never changed his ministry because someone found what he said as being &#8220;offensive.&#8221;  I guarantee, if you read through a Gospel from beginning to end, you will be offended by what Jesus says and does &#8211; and that is part of the message.  Love and grace is offensive if you believe you don&#8217;t need either, that you can make it in life on your own and that you can do no wrong.</p>
<p>I never saw the movie <strong>Love Story</strong> but I do remember a line made famous in it that says &#8220;Love is never having to say you are sorry.&#8221;  Doesn&#8217;t really work in marriage but it just may work in community life if you add, &#8220;Love is never having to say you are sorry if someone else is offended.&#8221;  Maybe they should think about why.  That is not being offended.  That is being honest.  And that is better for the church and a step toward being disciples of Jesus.</p>
