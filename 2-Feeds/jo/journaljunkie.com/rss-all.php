			<h2><a href="http://www.journaljunkie.com/2011/01/20/new-england-journal-of-medicine-acetaminophen-toxicity/" rel="bookmark" title="Permanent Link to New England Journal of Medicine: Acetaminophen Toxicity">New England Journal of Medicine: Acetaminophen Toxicity</a></h2> 
			<h3>January 20th, 2011</h3>
		</div>
		<div class="entrybody">
			<p><em></em>In partial response to a study published in the <em>New England Journal of Medicine</em> dated October 16, 1997 which revealed that &#8220;suicidal patients ingested almost twice as much acetaminophen as those in the accidental-overdose group, and that those who accidentally overdosed on acetaminophen showed greater levels of liver necrosis&#8221; the U.S. Food and Drug Administration (<a href="http://www.fda.gov/">USFDA</a>) announced on January 17, 2011 that it will &#8220;limit the active pain-reducing ingredient in the pain medications Percocet and Vicodin.&#8221;The administration went on to say they would order drug manufacturers to reduce the amount of acetaminophen to 325 milligrams per tablet (or dosage unit) in pain medications such as Percocet (Tylox) and Vicodin (Lortab).It is reported that the FDA plans to completely phase out what it calls &#8220;high dose&#8221; prescription medications that contain acetaminophen within a three year period.The Deputy Director of the Office of New drugs in the FDA&#8217;s Center for Drug and Evaluation Research (CDER) commented the &#8220;FDA is taking this action to make prescription combination pain medications containing acetaminophen safer for patients to use&#8221; and went on to state that &#8220;overdose from prescription combination products containing acetaminophen account for nearly half of all cases of acetaminophen-related liver failure in the United States; many of which result in liver transplant or death.&#8221;Interestingly, over-the-counter acetaminophen is not facing the same restrictions currently pursued for its prescription counterpart (since the FDA has stated that it has no intention of imposing such restrictions on over-the-counter drugs) which means that after the three-year roll out it is feasible that over-the-counter acetaminophen will actually be stronger than the acetaminophen prescribed by doctors.How&#8217;s that for logic.</p>
