<p>Turn is a 7/c Board is beginning to gel. Both blinds check and I bet out $120 into a $150 pot. Next 2 players fold, button wakes up and goes ALL-IN for $200. Both blinds fold. I&#8217;ve got the nuts and my decision now is to just smoothe call and possibly bring in the remaining player or come over the top for a total of $375 and push the remaining player out to limit the potential draws against me. WHAT WOULD YOU DO????????</p>
