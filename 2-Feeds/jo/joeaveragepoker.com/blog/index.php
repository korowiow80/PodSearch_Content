<p>This hand is from a $2/5 NL game at Nautica in the flats of Cleveland, Ohio.  I&#8217;m in mid-position with pocket Kings, Kc/Kd. There&#8217;s one limper, 2 fold and it&#8217;s on me and I make it $30 (pretty standard raise for this game).  I get 2 callers behind me and the limper in front of me calls.  There&#8217;s $127 in the pot.</p>
<p>The flop is 5h/2d/Qd.   Limper checks, I bet out $100 and get called by both players behind me.  Limper folds. The turn is 3h. Still decent for me.  I have about $450 in front of me. Now I need to decide what would be the best way to maximize this hand. Do I check and let one of the others make a move at this pot, or do I make an average bet or go all in and possibly end the hand here?  WHAT WOULD YOU DO??? </p>
