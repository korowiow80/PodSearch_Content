<p>This hand is from a recent $2/5 NL cash game. The game had just got off and I had close to $400 in front of me. I&#8217;m UTG and look down at 2 red nines. I raise it to $25 to go, pretty average bet for this game.  I end up with 5 callers, which include both SB, BB, button and 2 random mid position callers.</p>
